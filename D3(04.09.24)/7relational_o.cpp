#include <iostream>
using namespace std;
int main(){
    int a = 5;
    int b = 6;
    cout<< (a==b)<<endl; //False -> 0
    cout<< (a!=b)<<endl; //True -> 1
    cout<< (a<=b)<<endl; //True -> 1
    cout<< (a>=b)<<endl; //False -> 0
    cout<< (a>b)<<endl; //False -> 0
    cout<< (a<b)<<endl; //True -> 1
    return 0;
}